

public class Constructor {

String nome; 
private int numero;


public Constructor(int numero, String nome){
    this.numero = numero;
    this.nome = nome;
  
  

}
public void setNumero(int numero) {
    this.numero = numero;

}
    public int getNumero() {
        return numero;
     
}
public String toString() {
return nome + "nome" + numero + "numero";     
}

}
   





